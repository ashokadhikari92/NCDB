            <h2>What is Intranet System</h2>
            <p>An intranet is a private network that is contained within an
                enterprise. It may consist of many interlinked local area
                networks and also use leased lines in the wide area network.
                Typically, an intranet includes connections through one or more
                gateway computers to the outside Internet. The main purpose of an
                intranet is to share company information and computing resources
                among employees. An intranet can also be used to facilitate
                working in groups and for teleconferences.</p>
            <p>An intranet uses TCP/IP, HTTP, and other Internet protocols and
                in general looks like a private version of the Internet. With
                tunneling, companies can send private messages through the public
                network, using the public network with special
                encryption/decryption and other security safeguards to connect
                one part of their intranet to another. Typically, larger
                enterprises allow users within their intranet to access the
                public Internet through firewall servers that have the ability to
                screen messages in both directions so that company security is
                maintained. When part of an intranet is made accessible to
                customers, partners, suppliers, or others outside the company,
                that part becomes part of an extranet.</p>
